static uint8_t * p_cpuid_data	 = &CPU_ID;
static uint8_t * p_private_key = &PRIVATE_CART;

static uint8_t	 a_sign_dy[CERTIFICATION_SIG_MAX];
static size_t		 a_sign_size = 0;

/**
 * \brief  This function is used for testing purposes
 *
 * \return         none.
 */

extern uint32_t uwTick;

void sis_benchmark(void)
{
	uint32_t uwTickStart,uwTickEnd;
	//-------------------------------Aes128 CBC test---------------------------------

	sis_log(LogDebug,"AES-CBC test\r\n");
	//encrypt
	memcpy(_testIv,testIv,sizeof(testIv));	//restore iv
	encrypt_buffer_cbc(testKey,_testIv,testdat,sizeof(testdat), output_1);
	//decrypt
	memcpy(_testIv,testIv,sizeof(testIv));	//restore iv
	decrypt_buffer_cbc(testKey,_testIv,output_1, sizeof(output_1), output_2);
	
	if(memcmp(testdat,output_2,sizeof(testdat)) != 0){
		sis_log(LogError,"AES-CBC Error\r\n");
	}
	
	//Crc32
	sis_log(LogDebug,"Crc32 test\r\n");
	m_crc32 = crc32(testdat,sizeof(testdat));
	sis_log(LogDebug,"Crc32:0x%08x\r\n",m_crc32);
	
	//sign
	uwTickStart = uwTick;
	if(sissdk_ecdsa_sign(p_private_key,p_cpuid_data,96,a_sign_dy,&a_sign_size)){
		sis_log(LogDebug,"Sign ok...\r\n");
	}
	else{
		sis_log(LogDebug,"Sign Error...\r\n");
	}
	uwTickEnd = uwTick;
	sis_log(LogDebug,"---------------Elapsed time : %d ms (Base:64Mhz)---------------\r\n",uwTickEnd - uwTickStart);
	
	//verfiy sign
	uwTickStart = uwTick;
	if(sissdk_ecdsa_verify(p_public_key,p_cpuid_data,96,a_sign_dy,a_sign_size)){
		sis_log(LogDebug,"verify passed...\r\n");
	}
	else{
		sis_log(LogDebug,"verify error...\r\n");
	}
	uwTickEnd = uwTick;
	sis_log(LogDebug,"---------------Elapsed time : %d ms (Base:64Mhz)---------------\r\n",uwTickEnd - uwTickStart);
}

